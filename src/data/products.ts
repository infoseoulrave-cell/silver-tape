export function getProductBySlug(slug: string): Product | undefined {
  return PRODUCTS.find(p => p.slug === slug);
}

export function getProductById(id: string): Product | undefined {
  return PRODUCTS.find(p => p.id === id);
}

export function getProductsByCategory(category: CategoryId): Product[] {
  return PRODUCTS.filter(p => p.category === category);
}

export function getFeaturedProducts(): Product[] {
  return PRODUCTS.filter(p => p.featured);
}

/** 이번 주 기대작 — 클릭률 기반 큐레이션 */
const WEEKLY_PICK_IDS = ['artsy-001', 'artsy-002', 'sens-004', 'pop-001'];

export function getWeeklyPicks(): Product[] {
  return WEEKLY_PICK_IDS
    .map(id => PRODUCTS.find(p => p.id === id))
    .filter((p): p is Product => p !== undefined);
}

export function getAllSlugs(): string[] {
  return PRODUCTS.map(p => p.slug);
}

export function getProductsByStudio(studioSlug: string): Product[] {
  return PRODUCTS.filter(p => p.studioSlug === studioSlug);
}

export function getProductsByStudioAndCategory(studioSlug: string, category: CategoryId): Product[] {
  return PRODUCTS.filter(p => p.studioSlug === studioSlug && p.category === category);
}

export function getStudioSlugs(): string[] {
  return [...new Set(PRODUCTS.map(p => p.studioSlug))];
}
